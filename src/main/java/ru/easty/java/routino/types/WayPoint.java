public class WayPoint
{
	private double lat;
	private double lon;

	/**
	 *
	 * @return
	 */
	public double getLat()
	{
		return lat;
	}

	public void setLat( double lat )
	{
		this.lat = lat;
	}

	public double getLon()
	{
		return lon;
	}

	public void setLon( double lon )
	{
		this.lon = lon;
	}
}
